"""Phase 12-D3 prediction lineage / strict OOS integrity audit.

This module audits existing artifacts only. It does not train models,
regenerate predictions, run a full backtest, overwrite models, change profiles,
or call external APIs.
"""

from __future__ import annotations

import json
import math
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import pandas as pd


ROOT = Path(__file__).resolve().parents[2]
REPORT_STEM = "phase12d3_prediction_lineage_oos_audit"
TEST_2025_START = "2025-01-01"
TEST_2025_END = "2025-12-31"

VALUATION_MODEL_DIRS = {
    "valuation_phase11i_strict_oos": Path("models/ml/valuation_engine/research_phase11i_strict_oos"),
    "valuation_phase11b3_downside": Path("models/ml/valuation_engine/research_phase11b3_downside"),
    "valuation_candidate_phase11b": Path("models/ml/valuation_engine/candidate_phase11b"),
}

PHASE12_REPORT_STEMS = [
    "phase12a_dynamic_capital_allocation_2025",
    "phase12a2_allocation_score_refinement_2025",
    "phase12a3_top5_penalty_refinement_2025",
    "phase12b_limited_allocation_strategy_check_2025",
    "phase12b2_allocation_execution_adjustment_2025",
    "phase12b3_exit_hold_audit_2025",
    "phase12b4_trailing_exit_prototype_2025",
    "phase12b5_exit_threshold_recalibration_2025",
    "phase12c_dynamic_allocation_recalibrated_exit_2025",
    "phase12c2_utilization_without_dd_explosion_2025",
    "phase12c3_position_concentration_guard_2025",
    "phase12c4_concentration_guard_refinement_2025",
    "phase12d1_winning_to_losing_audit_2025",
    "phase12d2_buy_quality_reality_audit_2025",
]


@dataclass(frozen=True)
class Phase12D3Paths:
    markdown: Path
    json: Path


class Phase12D3PredictionLineageOOSAudit:
    def __init__(self, root: Path | str = ROOT) -> None:
        self.root = Path(root)

    def run(self) -> Phase12D3Paths:
        report = self.build_report()
        return self.save_outputs(report)

    def build_report(self) -> dict[str, Any]:
        artifacts = self.artifact_lineage_table()
        stock = self.stock_selection_lineage()
        valuation = self.valuation_lineage()
        downside = self.downside_lineage()
        phase12 = self.phase12_input_integrity(stock, valuation, downside)
        checklist = self.integrity_checklist(stock, valuation, downside, phase12)
        final = self.final_trust_decision(stock, valuation, downside, phase12, checklist)
        return {
            "metadata": self.metadata(),
            "executive_summary": self.executive_summary(final, checklist),
            "artifact_lineage_table": artifacts,
            "stock_selection_lineage": stock,
            "valuation_lineage": valuation,
            "downside_lineage": downside,
            "phase12_input_integrity": phase12,
            "final_trust_decision": final,
            "integrity_checklist": checklist,
            "recommendation": {
                "recommended_next_phase": final["recommended_next_phase"],
                "reason": final["reason"],
            },
        }

    def artifact_lineage_table(self) -> list[dict[str, Any]]:
        rows: list[dict[str, Any]] = []
        rows.append(
            {
                "artifact_path": "data/ml/valuation_engine/phase11a_valuation_dataset_2023-01_to_2026-05.parquet",
                "artifact_type": "dataset",
                "created_phase": "11-A",
                "training_start": None,
                "training_end": None,
                "validation_start": None,
                "validation_end": None,
                "test_start": None,
                "test_end": None,
                "prediction_start": "2023-01-04",
                "prediction_end": "2026-04-23",
                "uses_2025_training_data": False,
                "strict_oos_for_2025": None,
                "notes": "Feature/label dataset; strictness depends on downstream model split and stock selection prediction lineage.",
            }
        )
        rows.append(
            {
                "artifact_path": "data/ml/valuation_engine/phase12a_dynamic_capital_allocation_2025.parquet",
                "artifact_type": "scored_predictions",
                "created_phase": "12-A",
                "training_start": "2023-01-04",
                "training_end": "2023-12-31",
                "validation_start": "2024-01-01",
                "validation_end": "2024-12-31",
                "test_start": TEST_2025_START,
                "test_end": TEST_2025_END,
                "prediction_start": "2025-01-07",
                "prediction_end": "2025-12-29",
                "uses_2025_training_data": False,
                "strict_oos_for_2025": True,
                "notes": "Generated by Phase12-A from Phase11-B3 research strict OOS models.",
            }
        )
        for name, path in VALUATION_MODEL_DIRS.items():
            meta = self.read_json(path / "model_metadata.json")
            rows.append(self.model_artifact_row(name, path, meta))
        for stem in ["phase11i_strict_walk_forward_oos_2025", "phase11b3_expected_downside_model_2025", *PHASE12_REPORT_STEMS]:
            path = Path("reports/ml") / f"{stem}.json"
            report = self.read_json(path)
            rows.append(self.report_artifact_row(path, report))
        return rows

    def model_artifact_row(self, name: str, path: Path, meta: dict[str, Any] | None) -> dict[str, Any]:
        split = (meta or {}).get("split", {})
        train = split.get("train") or (meta or {}).get("train_period", {})
        validation = split.get("validation", {})
        test = split.get("test") or (meta or {}).get("test_period", {})
        training_end = train.get("end")
        uses_2025 = self.date_ge_2025(training_end)
        return {
            "artifact_path": str(path),
            "artifact_type": "model_dir",
            "created_phase": (meta or {}).get("phase"),
            "training_start": train.get("start"),
            "training_end": training_end,
            "validation_start": validation.get("start"),
            "validation_end": validation.get("end"),
            "test_start": test.get("start"),
            "test_end": test.get("end"),
            "prediction_start": None,
            "prediction_end": None,
            "uses_2025_training_data": uses_2025,
            "strict_oos_for_2025": bool((meta or {}).get("strict_model_oos") or split.get("strict_model_oos") or (training_end and training_end < TEST_2025_START)),
            "notes": "metadata found" if meta else "metadata missing",
            "name": name,
        }

    def report_artifact_row(self, path: Path, report: dict[str, Any] | None) -> dict[str, Any]:
        metadata = (report or {}).get("metadata", {})
        conditions = (report or {}).get("conditions", {})
        period = conditions.get("period", {})
        leakage = (report or {}).get("leakage_checklist", {})
        return {
            "artifact_path": str(path),
            "artifact_type": "report_json",
            "created_phase": metadata.get("phase") or self.phase_from_stem(path.stem),
            "training_start": self.nested_get(report, ["conditions", "split", "train", "start"]),
            "training_end": self.nested_get(report, ["conditions", "split", "train", "end"]),
            "validation_start": self.nested_get(report, ["conditions", "split", "validation", "start"]),
            "validation_end": self.nested_get(report, ["conditions", "split", "validation", "end"]),
            "test_start": self.nested_get(report, ["conditions", "split", "test", "start"]) or period.get("start"),
            "test_end": self.nested_get(report, ["conditions", "split", "test", "end"]) or period.get("end"),
            "prediction_start": self.nested_get(report, ["dataset_summary", "date_range", "min"]),
            "prediction_end": self.nested_get(report, ["dataset_summary", "date_range", "max"]),
            "uses_2025_training_data": bool(leakage.get("new_model_trained") is True and not self.nested_get(report, ["conditions", "strict_model_oos"])),
            "strict_oos_for_2025": self.report_strict_oos(report),
            "notes": "report present" if report else "report missing",
        }

    def stock_selection_lineage(self) -> dict[str, Any]:
        audit = self.read_json(Path("reports/ml/walk_forward_model_audit_5y_enriched_v2.json")) or {}
        summary = audit.get("summary", {})
        fold_rows = audit.get("fold_rows", [])
        rows_2025 = [row for row in fold_rows if str(row.get("month", "")).startswith("2025-")]
        all_train_before_test = bool(rows_2025) and all(row.get("effective_train_end_before_test_start") is True for row in rows_2025)
        code_path_ok = bool(summary.get("prediction_root_is_walk_forward") and summary.get("fold_specific_model_used_by_code_path") and not summary.get("current_model_used_by_walk_forward_code_path"))
        metadata_missing = int(summary.get("metadata_missing_files") or 0)
        unknown = not (audit and rows_2025 and code_path_ok)
        warnings = []
        if metadata_missing:
            warnings.append(f"{metadata_missing} walk-forward prediction files have no model_id metadata; relying on walk-forward audit code-path evidence.")
        return {
            "columns": ["stock_selection_rank_score", "risk_adjusted_score", "expected_return", "candidate_strength"],
            "source": "data/ml/walk_forward_predictions via Phase11-A valuation dataset builder",
            "source_code_reference": "src/ml/phase11a_valuation_dataset_audit.py",
            "walk_forward_audit_report": "reports/ml/walk_forward_model_audit_5y_enriched_v2.json",
            "model_train_period": "monthly walk-forward; each 2025 fold effective_train_end before test_start",
            "uses_2025_training_data": False if all_train_before_test and code_path_ok else None,
            "strict_oos_for_2025": bool(all_train_before_test and code_path_ok),
            "lineage_unknown": unknown,
            "warnings": warnings,
            "evidence": {
                "prediction_root_is_walk_forward": summary.get("prediction_root_is_walk_forward"),
                "fold_specific_model_used_by_code_path": summary.get("fold_specific_model_used_by_code_path"),
                "current_model_used_by_walk_forward_code_path": summary.get("current_model_used_by_walk_forward_code_path"),
                "2025_fold_count": len(rows_2025),
                "all_2025_effective_train_end_before_test_start": all_train_before_test,
                "metadata_missing_files": metadata_missing,
            },
        }

    def valuation_lineage(self) -> dict[str, Any]:
        b3_meta = self.read_json(Path("models/ml/valuation_engine/research_phase11b3_downside/model_metadata.json")) or {}
        i_meta = self.read_json(Path("models/ml/valuation_engine/research_phase11i_strict_oos/model_metadata.json")) or {}
        phase12a_code_uses_b3 = self.file_contains(Path("src/ml/phase12a_dynamic_capital_allocation.py"), "PHASE11B3_MODEL_DIR")
        strict = bool(b3_meta.get("strict_model_oos") and not self.model_uses_2025_training(b3_meta))
        return {
            "columns": ["opportunity_proba", "opportunity_rank_percentile", "opportunity_score_proba_rank"],
            "phase12_source_model": "models/ml/valuation_engine/research_phase11b3_downside/opportunity_top_decile_20d_classifier.joblib",
            "phase11i_reference_model": "models/ml/valuation_engine/research_phase11i_strict_oos/opportunity_top_decile_20d_classifier.joblib",
            "phase12a_code_uses_b3_model_dir": phase12a_code_uses_b3,
            "b3_split": b3_meta.get("split"),
            "phase11i_split": i_meta.get("split"),
            "uses_2025_training_data": self.model_uses_2025_training(b3_meta),
            "strict_oos_for_2025": strict,
            "lineage_unknown": not bool(b3_meta),
            "notes": "Phase12-A scored opportunity_proba using Phase11-B3 strict OOS research model.",
        }

    def downside_lineage(self) -> dict[str, Any]:
        b3_meta = self.read_json(Path("models/ml/valuation_engine/research_phase11b3_downside/model_metadata.json")) or {}
        strict = bool(b3_meta.get("strict_model_oos") and not self.model_uses_2025_training(b3_meta))
        return {
            "columns": ["downside_bad_proba", "downside_rank_percentile"],
            "phase12_source_model": "models/ml/valuation_engine/research_phase11b3_downside/downside_bad_20d_classifier.joblib",
            "b3_split": b3_meta.get("split"),
            "uses_2025_training_data": self.model_uses_2025_training(b3_meta),
            "strict_oos_for_2025": strict,
            "lineage_unknown": not bool(b3_meta),
            "notes": "Phase12-A scored downside_bad_proba using Phase11-B3 strict OOS research model.",
        }

    def phase12_input_integrity(self, stock: dict[str, Any], valuation: dict[str, Any], downside: dict[str, Any]) -> list[dict[str, Any]]:
        rows = []
        for stem in PHASE12_REPORT_STEMS:
            path = Path("reports/ml") / f"{stem}.json"
            report = self.read_json(path)
            leakage = (report or {}).get("leakage_checklist", {})
            metadata = (report or {}).get("metadata", {})
            rows.append(
                {
                    "report": str(path),
                    "phase": metadata.get("phase") or self.phase_from_stem(stem),
                    "report_exists": report is not None,
                    "uses_stock_selection_columns": self.report_mentions(report, ["stock_selection", "baseline_rank_score", "risk_adjusted_score", "expected_return", "candidate_strength"]),
                    "uses_valuation_columns": self.report_mentions(report, ["opportunity_proba", "opportunity_top_decile_proba", "opportunity_rank_percentile"]),
                    "uses_downside_columns": self.report_mentions(report, ["downside_bad_proba", "downside_rank_percentile"]),
                    "strict_oos_for_2025": bool(stock.get("strict_oos_for_2025") and valuation.get("strict_oos_for_2025") and downside.get("strict_oos_for_2025")),
                    "new_model_trained": bool(leakage.get("new_model_trained") or metadata.get("new_model_trained")),
                    "historical_predictions_regenerated": bool(leakage.get("historical_predictions_regenerated") or metadata.get("historical_predictions_regenerated")),
                    "existing_model_overwritten": bool(leakage.get("existing_model_overwritten") or metadata.get("existing_model_overwritten")),
                    "profile_changed": bool(leakage.get("profile_changed") or metadata.get("profile_modified") or metadata.get("profile_added")),
                    "full_backtest_executed": bool(leakage.get("full_backtest_executed") or metadata.get("full_backtest_executed")),
                    "blocking_issues": leakage.get("blocking_issues", []),
                }
            )
        return rows

    def integrity_checklist(self, stock: dict[str, Any], valuation: dict[str, Any], downside: dict[str, Any], phase12: list[dict[str, Any]]) -> dict[str, Any]:
        blocking = []
        if stock.get("lineage_unknown"):
            blocking.append("stock_selection_lineage_unknown")
        if valuation.get("lineage_unknown"):
            blocking.append("valuation_lineage_unknown")
        if downside.get("lineage_unknown"):
            blocking.append("downside_lineage_unknown")
        if stock.get("uses_2025_training_data") or valuation.get("uses_2025_training_data") or downside.get("uses_2025_training_data"):
            blocking.append("uses_2025_training_data_for_2025_eval")
        for row in phase12:
            for key in ["new_model_trained", "historical_predictions_regenerated", "existing_model_overwritten", "profile_changed", "full_backtest_executed"]:
                if row.get(key):
                    blocking.append(f"{row['phase']}:{key}")
        blocking = sorted(set(blocking))
        return {
            "new_model_trained": False,
            "historical_predictions_regenerated": False,
            "existing_model_overwritten": False,
            "profile_changed": False,
            "full_backtest_executed": False,
            "jquants_api_called": False,
            "openai_api_called": False,
            "stock_selection_lineage_unknown": bool(stock.get("lineage_unknown")),
            "valuation_lineage_unknown": bool(valuation.get("lineage_unknown")),
            "downside_lineage_unknown": bool(downside.get("lineage_unknown")),
            "uses_2025_training_data_for_2025_eval": bool(stock.get("uses_2025_training_data") or valuation.get("uses_2025_training_data") or downside.get("uses_2025_training_data")),
            "leakage_risk": "low" if not blocking else "high",
            "blocking_issues": blocking,
        }

    def final_trust_decision(self, stock: dict[str, Any], valuation: dict[str, Any], downside: dict[str, Any], phase12: list[dict[str, Any]], checklist: dict[str, Any]) -> dict[str, Any]:
        stock_ok = bool(stock.get("strict_oos_for_2025") and not stock.get("lineage_unknown"))
        valuation_ok = bool(valuation.get("strict_oos_for_2025") and not valuation.get("lineage_unknown"))
        downside_ok = bool(downside.get("strict_oos_for_2025") and not downside.get("lineage_unknown"))
        phase12_ok = stock_ok and valuation_ok and downside_ok and not checklist["blocking_issues"] and all(row.get("strict_oos_for_2025") for row in phase12 if row.get("report_exists"))
        if phase12_ok:
            next_phase = "Phase12-D4 Exit AI Dataset Audit"
            reason = "Stock Selection, Valuation, and Downside inputs are supported as strict OOS for 2025 by existing lineage evidence."
        elif not stock_ok:
            next_phase = "Phase12-D4 Stock Selection Strict OOS Audit"
            reason = "Stock Selection lineage is not fully trustworthy for 2025."
        elif not valuation_ok:
            next_phase = "Phase12-D4 Valuation Prediction Regeneration"
            reason = "Valuation predictions are not verified as strict OOS for 2025."
        elif not downside_ok:
            next_phase = "Phase12-D4 Downside Prediction Regeneration"
            reason = "Downside predictions are not verified as strict OOS for 2025."
        else:
            next_phase = "Phase12-D4 Stop / Redesign"
            reason = "Integrity blockers remain."
        return {
            "stock_selection_strict_oos_for_2025": stock_ok,
            "valuation_strict_oos_for_2025": valuation_ok,
            "downside_strict_oos_for_2025": downside_ok,
            "phase12_results_trustworthy": phase12_ok,
            "blocking_issues": checklist["blocking_issues"],
            "recommended_next_phase": next_phase,
            "reason": reason,
        }

    def executive_summary(self, final: dict[str, Any], checklist: dict[str, Any]) -> str:
        if final["phase12_results_trustworthy"]:
            return "Phase12 2025 inputs are supported as strict OOS by existing artifacts; proceed to Exit AI Dataset Audit."
        return f"Phase12 2025 input integrity is not fully trusted: {checklist['blocking_issues']}"

    def metadata(self) -> dict[str, Any]:
        return {
            "phase": "12-D3",
            "scope": "prediction lineage and strict OOS integrity audit only",
            "new_model_trained": False,
            "historical_predictions_regenerated": False,
            "existing_model_overwritten": False,
            "profile_changed": False,
            "full_backtest_executed": False,
            "jquants_api_called": False,
            "openai_api_called": False,
        }

    def read_json(self, path: Path) -> dict[str, Any] | None:
        full = self.root / path
        if not full.exists():
            return None
        return json.loads(full.read_text(encoding="utf-8"))

    def file_contains(self, path: Path, text: str) -> bool:
        full = self.root / path
        return full.exists() and text in full.read_text(encoding="utf-8")

    def date_ge_2025(self, date: str | None) -> bool:
        return bool(date and date >= TEST_2025_START)

    def model_uses_2025_training(self, meta: dict[str, Any]) -> bool:
        split = meta.get("split", {})
        train = split.get("train") or meta.get("train_period", {})
        return self.date_ge_2025(train.get("end"))

    def report_strict_oos(self, report: dict[str, Any] | None) -> bool | None:
        if not report:
            return None
        text = json.dumps(report)
        if "strict_model_oos" in text:
            return '"strict_model_oos": true' in text
        leakage = report.get("leakage_checklist", {})
        metadata = report.get("metadata", {})
        return not any(
            bool(leakage.get(key) or metadata.get(key))
            for key in ["new_model_trained", "historical_predictions_regenerated", "existing_model_overwritten", "profile_changed", "full_backtest_executed"]
        )

    def report_mentions(self, report: dict[str, Any] | None, needles: list[str]) -> bool:
        if not report:
            return False
        text = json.dumps(report)
        return any(needle in text for needle in needles)

    def nested_get(self, data: dict[str, Any] | None, path: list[str]) -> Any:
        current: Any = data
        for key in path:
            if not isinstance(current, dict):
                return None
            current = current.get(key)
        return current

    def phase_from_stem(self, stem: str) -> str | None:
        if stem.startswith("phase"):
            return stem.split("_", 1)[0].replace("phase", "").upper()
        return None

    def save_outputs(self, report: dict[str, Any]) -> Phase12D3Paths:
        report_dir = self.root / "reports/ml"
        report_dir.mkdir(parents=True, exist_ok=True)
        json_path = report_dir / f"{REPORT_STEM}.json"
        markdown_path = report_dir / f"{REPORT_STEM}.md"
        json_path.write_text(json.dumps(report, ensure_ascii=False, indent=2, default=str), encoding="utf-8")
        markdown_path.write_text(self.to_markdown(report), encoding="utf-8")
        return Phase12D3Paths(markdown=markdown_path, json=json_path)

    def to_markdown(self, report: dict[str, Any]) -> str:
        lines = [
            "# Phase 12-D3 Prediction Lineage / Strict OOS Integrity Audit",
            "",
            "## Executive Summary",
            "",
            str(report.get("executive_summary")),
            "",
            "## Final Trust Decision",
            "",
            self.table([report["final_trust_decision"]], ["stock_selection_strict_oos_for_2025", "valuation_strict_oos_for_2025", "downside_strict_oos_for_2025", "phase12_results_trustworthy", "blocking_issues", "recommended_next_phase", "reason"]),
            "",
            "## Artifact Lineage Table",
            "",
            self.table(report.get("artifact_lineage_table", []), ["artifact_path", "artifact_type", "created_phase", "training_start", "training_end", "validation_start", "validation_end", "test_start", "test_end", "prediction_start", "prediction_end", "uses_2025_training_data", "strict_oos_for_2025", "notes"]),
            "",
            "## Stock Selection Lineage",
            "",
            self.table([report["stock_selection_lineage"]], ["source", "strict_oos_for_2025", "lineage_unknown", "uses_2025_training_data", "warnings", "evidence"]),
            "",
            "## Valuation Lineage",
            "",
            self.table([report["valuation_lineage"]], ["phase12_source_model", "strict_oos_for_2025", "lineage_unknown", "uses_2025_training_data", "notes"]),
            "",
            "## Downside Lineage",
            "",
            self.table([report["downside_lineage"]], ["phase12_source_model", "strict_oos_for_2025", "lineage_unknown", "uses_2025_training_data", "notes"]),
            "",
            "## Phase12 Input Integrity",
            "",
            self.table(report.get("phase12_input_integrity", []), ["report", "phase", "report_exists", "strict_oos_for_2025", "new_model_trained", "historical_predictions_regenerated", "existing_model_overwritten", "profile_changed", "full_backtest_executed", "blocking_issues"]),
            "",
            "## Integrity Checklist",
            "",
            self.table([report["integrity_checklist"]], ["new_model_trained", "historical_predictions_regenerated", "existing_model_overwritten", "profile_changed", "full_backtest_executed", "jquants_api_called", "openai_api_called", "stock_selection_lineage_unknown", "valuation_lineage_unknown", "downside_lineage_unknown", "uses_2025_training_data_for_2025_eval", "leakage_risk", "blocking_issues"]),
            "",
            "## Recommendation",
            "",
            self.table([report["recommendation"]], ["recommended_next_phase", "reason"]),
            "",
        ]
        return "\n".join(lines)

    def table(self, rows: list[dict[str, Any]], columns: list[str]) -> str:
        if not rows:
            return "_No rows._"
        header = "| " + " | ".join(columns) + " |"
        sep = "| " + " | ".join("---" for _ in columns) + " |"
        body = ["| " + " | ".join(self.format_value(row.get(column)) for column in columns) + " |" for row in rows]
        return "\n".join([header, sep, *body])

    def format_value(self, value: Any) -> str:
        if isinstance(value, float):
            if math.isnan(value):
                return ""
            return f"{value:.4f}"
        if isinstance(value, (dict, list)):
            return json.dumps(value, ensure_ascii=False)
        if value is None:
            return ""
        return str(value)
